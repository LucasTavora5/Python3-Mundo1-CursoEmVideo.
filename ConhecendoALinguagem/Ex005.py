# Faça um programa que leia um número inteiro e mostre na tela o seu sucessor e seu antecessor:
n1 = int(input('Digite um número para saber seu antecessor e sucessor: '))#entrada
ant = n1-1 #funçoes
suc = n1+1
print('O número inserido foi {}, seu antecessor é {} e o sucessor {}!'.format(n1,ant,suc))#saída