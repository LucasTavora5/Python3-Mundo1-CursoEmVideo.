# Crie um script Python que leia dois números e tente mostrar a soma entre eles
n1 = int(input('Digite um número inteiro: '))
n2 = int(input('Digite outro número inteiro para obter a soma entre eles: '))
soma = n1+n2 #função de soma
print('O primeiro número foi o {}, o segundo foi o {} e a soma entre eles é: {}'.format(n1,n1,soma))